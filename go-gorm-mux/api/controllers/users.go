// CreateUser controller is responsible for creating a new user.
func CreateUser(w http.ResponseWriter, r *http.Request) {
	body, err := ioutil.ReadAll(r.Body)
	if err != nil {
		responses.ERROR(w, http.StatusUnprocessableEntity, err)
	}

	user := models.User{}
	err = json.Unmarshal(body, &user)
	if err != nil {
		responses.ERROR(w, http.StatusUnprocessableEntity, err)
		return
	}

	user.Prepare()
	err = user.Validate("")
	if err != nil {
		responses.ERROR(w, http.StatusUnprocessableEntity, err)
		return
	}

	userCreated, err := user.SaveUser(database.DB)

	if err != nil {
		formattedError := formaterror.FormatError(err.Error())
		responses.ERROR(w, http.StatusInternalServerError, formattedError)
		return
	}

	w.Header().Set("Location", fmt.Sprintf("%s%s/%d", r.Host, r.RequestURI, userCreated.ID))
	responses.JSON(w, http.StatusCreated, userCreated)
}

// GetUsers controller is responsible for getting all users.
func GetUsers(w http.ResponseWriter, r *http.Request) {
	user := models.User{}

	// Generate pagination parameters.
	pagination := pagination.GeneratePaginationFromRequest(r)

	// Get all users with pagination.
	users, err := user.FindAllUsers(database.DB, &pagination)
	if err != nil {
		responses.ERROR(w, http.StatusInternalServerError, err)
		return
	}

	// Build pagination response.
	result := responses.ResponseJSON{
		Data: users,
		Meta: responses.Meta{
			Pagination: responses.Pagination{
				Total:  int(pagination.TotalRows),
				Limit:  pagination.Limit,
				Offset: pagination.GetOffset(),
			},
		},
	}

	responses.JSON(w, http.StatusOK, result)
}

